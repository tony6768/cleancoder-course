package com.globalretailtech.pos.events;


import com.globalretailtech.pos.events.PosEvent;

/**
 * Execute the event on the top of the stack.
 *
 * @author  Quentin Olson
 */
public class EnterKey extends PosEvent {

    /** Simple constructor for dynamic loading. */
    public EnterKey() {
    }

    public void engage(int value) {
		
		context().eventStack().dump();
		
        // next event pops the top of the stack and executes it.

        context().eventStack().nextEvent();

        // the previous event should have picked up
        // what it needed, so clear the input buffer.

        context().clearInput();
    }

    /** Always return true. */
    public boolean validTransition(String event) {
        return true;
    }

    /** Clear impementation for clear, do nothing. */
    public void clear() {
    }

    /** Always return true. */
    public boolean checkProfile(int event) {
        return true;
    }

    private static String eventname = "EnterKey";

    /** Return staic name. */
    public String toString() {
        return eventname;
    }

    /** Return staic name. */
    public static String eventName() {
        return eventname;
    }
}


